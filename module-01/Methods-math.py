import math

print(round(1.3))  # Redondea al entero más cercano
print(abs(-10))  # Valor absoluto
print(math.ceil(1.3))  # Redondea hacia arriba
print(math.floor(1.3))  # Redondea hacia abajo
print(math.sqrt(16))  # Raíz cuadrada
print(math.pow(2, 3))  # Potencia: 2 elevado a 3
print(math.pi)  # Valor de pi
print(math.e)  # Valor de euler
print(math.factorial(5))  # Factorial de 5
print(math.gcd(12, 15))  # Máximo común divisor de 12 y 15
print(math.lcm(12, 15))  # Mínimo común múltiplo de 12 y 15
print(math.log(100, 10))  # Logaritmo base 10 de 100
print(math.sin(math.pi / 2))  # Seno de pi/2
print(math.cos(0))  # Coseno de 0
print(math.tan(math.pi / 4))  # Tangente de pi/4
print(math.degrees(math.pi))  # Convierte radianes a grados